namespace GingaGame_MonoGame.GameLogic;

/// <summary>
///     Enumerates the game modes available in the game.
/// </summary>
public enum GameMode
{
    /// <summary>
    ///     Represents the mode 1 of the game.
    ///     Merge planets to get a star! Strategically drop and combine planets to unlock new types and battle against rising
    ///     chaos. Reach the sun to win.
    /// </summary>
    Mode1,

    /// <summary>
    ///     Represents the second game mode: Reverse evolution! Combine planets to shrink them, navigating a multi-level
    ///     funnel. Reach the bottom with the smallest planet to win.
    /// </summary>
    Mode2
}